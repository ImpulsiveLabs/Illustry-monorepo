const normalizeLocale = (candidate?: string | null): Locale | undefined => {
  if (!candidate) {
    return undefined;
  }

  const normalized = candidate.toLowerCase().replace('_', '-');
  if ((SUPPORTED_LOCALES as readonly string[]).includes(normalized)) {
    return normalized as Locale;
  }

  const byPrefix = SUPPORTED_LOCALES.find((locale) => normalized.startsWith(`${locale}-`));
  return byPrefix;
};

const resolveLocaleFromLanguage = (language?: string | null): Locale => {
  const normalized = normalizeLocale(language);
  if (normalized) {
    return normalized;
  }
  return 'en';
};

